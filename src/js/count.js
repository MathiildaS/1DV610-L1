/**
 * A function to count vowels and consonants in a word
 *
 * @author Mathilda Segerlund <ms228qs@student.lnu.se>
 * @version 1.0.0
 */

export function countVowelsConsonants (name) {
  let vowels = 0
  let consonants = 0

  for (let i = 0; i < name.length; i++) {
    if (i === a, e, i, o, u, å, ä, ö) {
      vowels += 1 
    } else {
      consonants += 1
    }
  }
}